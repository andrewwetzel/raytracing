      SUBROUTINE FGSW(X, F, DF, G, DG)
      COMPLEX F, DF, G, DG
      REAL X, X2, X3, T2, T3, T4, T8, T12, T16
      
      CALL FSW(X, F, DF)
      
      IF (ABS(X) .GT. 50.0) GO TO 1
      
      G = X * F
      DG = F + X * DF
      RETURN
      
 1    X2 = X * X
      X3 = X2 * X
      T2 = 2.0 * X2
      T3 = 3.0 * X2
      T4 = 4.0 * X2
      T8 = 8.0 * X2
      T12 = 12.0 * X2
      T16 = 16.0 * X2
      
      G = CMPLX(1.0 - 35.0/T4*(1.0 - 99.0/T4*(1.0 - 195.0/T4*
     1          (1.0 - 323.0/T4)))/T4,
     2          2.5*(1.0 - 63.0/T4*(1.0 - 143.0/T4*(1.0 - 255.0/T4*
     3          (1.0 - 399.0/T4))))/X)
      
      DG = 0.5 * CMPLX(35.0*(1.0 - 99.0/T2*(1.0 - 585.0/T8*
     1         (1.0 - 323.0/T3*(1.0 - 2415.0/T16))))/X3,
     2         -5.0*(1.0 - 189.0/T4*(1.0 - 115.0/T12*(1.0 - 357.0/T4*
     3         (1.0 - 513.0/T4))))/X2)
     
      RETURN
      END
